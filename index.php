        <h2>NOSSA HISTÓRIA</h2>
        <p>Somos um grupo de estudantes da ETEC Professor Camargo Aranha, e percebemos a terrível situação que se encontram as instituições públicas e privadas de abrigos de cães e gatos. Muitas dessas instituições estão sem alimentos, brinquedos, cobertores ou espaço.</p>
        <p>Sendo assim, decidimos criar essa plataforma como nosso trabalho de conclusão de curso, para alavancar meios de ajudar essas instalações, conectando essas ONG's com os doadores, para apoiar como puder de acordo com cada caso.</p>
        <p>A iniciativa Ártemis foi pensada em setembro de 2019 e fundada em julho de 2020, esperamos continuar desenvolvendo mais recursos que possam auxiliar nossos pets a conseguir mais amor e carinho que eles merecem.</p>
